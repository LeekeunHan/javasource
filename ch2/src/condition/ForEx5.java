package condition;

public class ForEx5 {

	public static void main(String[] args) {
		// 구구단 

		//2단 출력 
		for (int i=1; i<10; i++) {
			System.out.printf("2 * %d = %d\t",i,(2*i));
		}
		
		System.out.println(); // 한줄 띄우기 
		
		// 3단 출력 
		for (int i=1; i<10; i++) {
			System.out.printf("3 * %d = %d\n",i,(3*i));
		}

	}

}
