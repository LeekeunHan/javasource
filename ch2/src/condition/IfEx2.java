package condition;

/*
 * if (조건식) {
 * 
 * } else{ 조건이 거짓일때 
 * 
 * }
 * 
 * */
 

public class IfEx2 {

	public static void main(String[] args) {
		int score = 90;	
		
		// score가 90 이상이면 메세지 출력
		// score가 90 미만이면 메시지 출력 
		if (score >= 90) {
			System.out.println("점수는 90점 이상입니다");
		}else {
			System.out.println("점수는 90점 미만입니다");
		}

		
		
	}

}
